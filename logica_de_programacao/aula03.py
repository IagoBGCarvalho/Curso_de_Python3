"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica/Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""

print(1234) # Dinâmica = Já sabe que é um numero inteiro

# Aspas simples
print('Iago Carvalho')

# Aspas duplas
print("Iago Carvalho")

# Escape (O interpretador pula apos a \)
print("Iago \"Carvalho\"")

# r
print(r"Iago \"Carvalho\"")

# Modo limpo
print("Iago 'Carvalho'")
