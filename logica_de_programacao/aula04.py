# Tipos int e float
# int -> Numero inteiro
# O tipo int representa qualquer numero positivo ou negativo
# int sem sinal = positivo
print(11) # int
print(-11) # int
print(0) # int

# float -> Numero com ponto flutuante
# O tipo float representa qualquer numero positivo ou negativo com ponto flutuante
# float sem sinal = positivo
print(1.1, 10.11) # float
print(0.0, -1.5) # float

# A função(classe) type mostra o tipo que o python inferiu ao valor
print(type("Iago"))
print(type(1))
print(type(1.1), type(-1.1), type(0.0))